package aula;

public class Calculos {

	public double soma(double x, double y){
		double total = 0;
		
		total = x + y;
		return total;
	}
	
	public double subtracao(double x, double y){
		double total = 0;
		
		total = x - y;
		return total;
	}
	
	public double multiplicao(double x, double y){
		double total = 0;
		
		total = x * y;
		return total;
	}
	
	public double divisao(double x, double y){
		double total = 0;
		
		total = x / y;		
		return total;
	}
}
